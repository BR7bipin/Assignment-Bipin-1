n1 = int(float(input("Enter first number: ")))
n2 = int(float(input("Enter second number: ")))

print(f"\nNumbers: {n1}, {n2}\n")

print("Calculations:".upper())
print("Addition:", n1 + n2)
print("Substraction:", n1 - n2)
print("Multiplication:", n1 * n2)
print("Float Division:", n1 / n2)
print("Integer Division:", n1 // n2)
print("Modulus:", n1 % n2)
print("Power:", n1 ** n2)
